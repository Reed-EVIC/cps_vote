                                      "Asian or Pacific Islander")),
         CPS_HISP = factor(CPS_HISP, levels = 1:2,
                           labels = c("HISPANIC",
                                      "NON-HISPANIC")),
         VRS_VOTE = factor(VRS_VOTE, levels = c(1,2,-1,-2,-3,-9),
                           labels = c("Yes",
                                      "No",
                                      "Not in Universe",
                                      "Don't know",
                                      "Refused",
                                      "No Response")),
         VRS_VOTEREG = factor(VRS_VOTEREG, levels = c(1, 2, -1, -2, -3, -9),
                              labels = c("Yes",
                                         "No",
                                         "Not in Universe",
                                         "Don't know",
                                         "Refused",
                                         "No Response")),
         VRS_VOTETIME = factor(VRS_VOTETIME, levels = c(1:5, -1, -2, -3, -9),
                               labels = c("Before noon",
                                          "Noon to 4 p.m.",
                                          "4 p.m. to 6 p.m.",
                                          "After 6 p.m.",
                                          "Voted absentee",
                                          "Not in Universe",
                                          "Don't know",
                                          "Refused",
                                          "No Response")),
         VRS_RESIDENCE = factor(VRS_RESIDENCE, levels = c(1:6, -1, -2, -3, -9),
