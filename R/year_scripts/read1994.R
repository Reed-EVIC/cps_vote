# read the file
# start and end locations via 'docs/doc1994.pdf'
cpsvrs1994_orig <- readr::read_fwf(here::here('data', 'data1994.txt'),
                                   readr::fwf_cols(CPS_YEAR = c(67, 68),
                                                   CPS_STATE = c(93, 94),
                                                   CPS_AGE = c(122, 123),
                                                   CPS_SEX = c(129, 130),
                                                   CPS_EDU = c(137, 138),
                                                   CPS_RACE = c(139, 140),
                                                   CPS_HISP = c(157,158),
                                                   WEIGHT = c(613, 622),
                                                   VRS_VOTE = c(817, 818),
                                                   VRS_VOTEREG = c(819, 820),
                                                   VRS_VOTETIME = c(821, 822),
                                                   VRS_RESIDENCE = c(823, 824))) %>%
  mutate(CPS_YEAR = CPS_YEAR + 1900)

# set the factors
