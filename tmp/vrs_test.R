                      labels = c("In person",
                                 "By mail",
                                 "Not in Universe",
                                 "Don't know",
                                 "Refused",
                                 "No Response")),
         ELEXDAY = factor(ELEXDAY, levels = c(1, 2, -1, -2, -3, -9),
                          labels = c("On election day",
                                     "Before election day",
                                     "Not in Universe",
