                                  "Black-HP",
                                  "AI-Asian",
                                  "AI-HP",
                                  "Asian-HP",
                                  "W-B-AI",
                                  "W-B-A",
                                  "W-B-HP",
                                  "W-AI-A",
                                  "W-AI-HP",
                                  "W-A-HP",
                                  "B-AI-A",
                                  "W-B-AI-A",
                                  "W-AI-A-HP",
                                  "Other 3 Race Combinations",
                                  "Other 4 and 5 Race Combinations")),
         HISP = factor(HISP, levels = 1:2, labels = c("HISPANIC", "NON_HISPANIC")),
         VOTE = factor(VOTE, levels = c(1,2,-1,-2,-3,-9),
                       labels = c("Yes",
