# script to actually start pulling the relevant file, the CPS voter supplement

library(readr)
library(here)
library(dplyr)
library(maps)

cpsvrs18 <- read_fwf(here('data', 'nov18pub.dat'),
                     fwf_cols(YEAR = c(18, 21),
                              STATE = c(93, 94),
                              AGE = c(122, 123),
                              SEX = c(129, 130),
                              EDU = c(137,138),
                              RACE = c(139, 140),
                              HISP = c(157,158),
                              WEIGHT = c(613, 622),
                              VOTE = c(1001, 1002),
                              VOTEREG = c(1003, 1004),
                              NOREGWHY = c(1005, 1006),
                              NOVOTEWHY = c(1007, 1008),
                              VBM = c(1009, 1010),
                              ELEXDAY = c(1011, 1012),
                              REGHOW = c(1013, 1014),
                              RESIDENCE = c(1015, 1016)))

fips <- tigris::fips_codes %>%
  select(starts_with('state')) %>%
  distinct() %>%
  filter(state_code < 57) # only states plus DC

cpsvrs <- bind_rows(cpsvrs18) %>%
  filter(WEIGHT > 0) %>% # zero-weight people have all NA codes (-1) on VRS questions
  mutate(STATE = factor(STATE, levels = fips$state_code, labels = fips$state),
         AGE = ifelse(AGE < 0, NA, AGE),
         SEX = factor(SEX, levels = 1:2, labels = c("MALE", "FEMALE")),
